package Operations;

public class Student {
    
    private String dni;
    private String name;
    private String surname;
    private int age;

    public Student(String dni, String name, String surname, int age){
        this.dni = dni;
        this.name = name;
        this.surname = surname;
        this.age = age;
    }//Student Constructor

    public String getDni() {
        return dni;
    }//getDni

    public void setDni(String dni) {
        this.dni = dni;
    }//setDni

    public String getName() {
        return name;
    }//getName

    public void setName(String name) {
        this.name = name;
    }//setName

    public String getSurname() {
        return surname;
    }//getSurname

    public void setSurname(String surname) {
        this.surname = surname;
    }//setSurname

    public int getAge() {
        return age;
    }//getAge

    public void setAge(int age) {
        this.age = age;
    }//setAge

    @Override
    public String toString() {
        return "Student [age=" + age + ", dni=" + dni + ", name=" + name + ", surname=" + surname + "]";
    }

}//class
